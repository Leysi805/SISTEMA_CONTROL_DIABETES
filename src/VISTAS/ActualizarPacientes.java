    void actualizar(){
        int fila = tablaPaciente.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }
        else{           
            String nombre = txtNombre.getText();
            String fecha_nacimiento = txtFechaN.getText();
            String genero;
            if (rdbMasculino.isSelected()) {
        genero = "M";
    } else if (rdbFemenino.isSelected()) {
        genero = "F";
    } else {
        // Manejar el caso en el que no se seleccionÃ³ ninguno
        genero = "N";
    }
            String domicilio = txtDireccion.getText();
            String telefono = txtTelefono.getText();
            String contacto_emergencia = txtContacto.getText();
            
            // Crear un objeto paciente
            paciente p = new paciente();
            p.setId_paciente(id_paciente);
            p.setNombre(nombre);
            p.setFecha_nacimiento(fecha_nacimiento);
            p.setGenero(genero);
            p.setDomicilio(domicilio);
            p.setTelefono(telefono);
            p.setContacto_emergencia(contacto_emergencia);

            // Verificar si algÃºn campo obligatorio estÃ¡ vacÃ­o
    if (camposVacios()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // No se realiza la inserciÃ³n si hay campos vacÃ­os
    }
    
            // Llamar al mÃ©todo actualizar de tu pacienteDAO
            int resultado = dao.actualizar(p);

            // Verificar si la actualizaciÃ³n fue exitosa
            if (resultado > 0) {
                JOptionPane.showMessageDialog(this, "Registro actualizado correctamente");
                listar(); // Actualizar la tabla despuÃ©s de la actualizaciÃ³n
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar el registro");
            }
        }
        
        limpiar();
        
    }
    
    // Verificar si algÃºn campo obligatorio estÃ¡ vacÃ­o
    boolean camposVacios() {
    return txtNombre.getText().isEmpty() || txtFechaN.getText().isEmpty() || txtDireccion.getText().isEmpty() || txtTelefono.getText().isEmpty();
}
    
