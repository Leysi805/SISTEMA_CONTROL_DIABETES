 void actualizar(){
        int fila = tablaTratamiento.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }
        else{           
            //Obtener el nombre y despues el id
            String nombrePaciente = (String) cmbPaciente.getSelectedItem();
            int id_paciente = daot.obtenerIdPacientePorNombre(nombrePaciente);
            
            String fecha = txtFecha.getText();
            String tipo_diabetes = (String) cmbTipo.getSelectedItem();
            String condiciones_medicas = txaCondiciones.getText();
            String medicamentos_dosis = txaMedicamentos.getText();
            String nivel_glucosa = txtGlucosa.getText();
            Double peso = Double.parseDouble(txtPeso.getText());
            String descripcion_dieta = txaDieta.getText();
            
            // Crear un objeto tratamiento
            tratamiento t = new tratamiento();
            t.setId_tratamiento(id_tratamiento);
            t.setId_paciente(id_paciente);
            t.setFecha(fecha);
            t.setTipo_diabetes(tipo_diabetes);
            t.setCondiciones_medicas(condiciones_medicas);
            t.setMedicamentos_dosis(medicamentos_dosis);
            t.setNivel_glucosa(nivel_glucosa);
            t.setPeso(peso);
            t.setDescripcion_dieta(descripcion_dieta);

            // Verificar si algÃºn campo obligatorio estÃ¡ vacÃ­o
    if (camposVacios()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // No se realiza la inserciÃ³n si hay campos vacÃ­os
    }
    
            // Llamar al mÃ©todo actualizar de tratamientoDAO
            int resultado = daot.actualizar(t);

            // Verificar si la actualizaciÃ³n fue exitosa
            if (resultado > 0) {
                JOptionPane.showMessageDialog(this, "Registro actualizado correctamente");
                listar(); // Actualizar la tabla despuÃ©s de la actualizaciÃ³n
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar el registro");
            }
        }
        
        limpiar();
        
    }
    
    // Verificar si algÃºn campo obligatorio estÃ¡ vacÃ­o
    boolean camposVacios() {
    return txtFecha.getText().isEmpty() || txtGlucosa.getText().isEmpty() || txtPeso.getText().isEmpty() || 
            txaCondiciones.getText().isEmpty() || txaMedicamentos.getText().isEmpty() || txaDieta.getText().isEmpty();
}
    
