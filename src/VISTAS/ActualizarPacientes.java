    // Rellenar tabla con los datos registrados
void listar() {
    List<paciente> lista = dao.listar();
    modelo = (DefaultTableModel) tablaPaciente.getModel();
    modelo.setRowCount(0); // Limpia la tabla antes de agregar nuevos datos
    
    Object[] ob = new Object[7];
    
    for (int i = 0; i < lista.size(); i++) {
        ob[0] = lista.get(i).getId_paciente();
        ob[1] = lista.get(i).getNombre();
        ob[2] = lista.get(i).getFecha_nacimiento();
        ob[3] = lista.get(i).getGenero();
        ob[4] = lista.get(i).getDomicilio();
        ob[5] = lista.get(i).getTelefono();
        ob[6] = lista.get(i).getContacto_emergencia();
        modelo.addRow(ob);
    }
    
    tablaPaciente.setModel(modelo);
    System.out.println("Listado de pacientes completado.");
}

