    private void tablaPacienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPacienteMouseClicked
        // TODO add your handling code here:
        int fila = tablaPaciente.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        } else {
            id_paciente = Integer.parseInt(tablaPaciente.getValueAt(fila, 0).toString());
            String nombre = tablaPaciente.getValueAt(fila, 1).toString();
            String fecha_nacimiento = tablaPaciente.getValueAt(fila, 2).toString();
            String genero = tablaPaciente.getValueAt(fila, 3).toString();
            String domicilio = tablaPaciente.getValueAt(fila, 4).toString();
            String telefono = tablaPaciente.getValueAt(fila, 5).toString();
            String contacto_emergencia = tablaPaciente.getValueAt(fila, 6).toString();
            
            //Configurar el nombre
            txtNombre.setText(nombre);
            
            //Configurar la fecha de nacimiento
            txtFechaN.setText(fecha_nacimiento);
            
            //Configurar el genero
            if ("M".equals(genero)) {
            rdbMasculino.setSelected(true);
            rdbFemenino.setSelected(false);
        } else {
            rdbMasculino.setSelected(false);
            rdbFemenino.setSelected(true);
        }
            
            //Configurar el domicilio
            txtDireccion.setText(domicilio);
            
            //Configurar el telefono
            txtTelefono.setText(telefono);
            
            //Configurar el contacto de emergencia
            txtContacto.setText(contacto_emergencia);
        }
    }//GEN-LAST:event_tablaPacienteMouseClicked

