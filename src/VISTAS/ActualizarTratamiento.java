    void listar() {
    List<tratamiento> lista = daot.listar();
    modelo = (DefaultTableModel) tablaTratamiento.getModel();
    modelo.setRowCount(0); // Limpia la tabla antes de agregar nuevos datos
    // Limpiamos el ComboBox antes de agregar nuevos elementos
    cmbPaciente.removeAllItems();
    // Obtenemos la lista de pacientes
    List<String> listaPacientes = daot.listarPacientes();

    // Agregamos los nombres de los pacientes al ComboBox
    for (String nombrePaciente : listaPacientes) {
        cmbPaciente.addItem(nombrePaciente);
    }
    
    Object[] ob = new Object[9];
    
    for (int i = 0; i < lista.size(); i++) {
        ob[0] = lista.get(i).getId_tratamiento();
        ob[1] = lista.get(i).getId_paciente();
        ob[2] = lista.get(i).getFecha();
        ob[3] = lista.get(i).getTipo_diabetes();
        ob[4] = lista.get(i).getCondiciones_medicas();
        ob[5] = lista.get(i).getMedicamentos_dosis();
        ob[6] = lista.get(i).getNivel_glucosa();
        ob[7] = lista.get(i).getPeso();
        ob[8] = lista.get(i).getDescripcion_dieta();
        modelo.addRow(ob);
    }
    
    tablaTratamiento.setModel(modelo);
    System.out.println("Listado de tratamientos completado.");
}
