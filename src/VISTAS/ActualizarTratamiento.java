    private void tablaTratamientoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaTratamientoMouseClicked
        // TODO add your handling code here:
        int fila = tablaTratamiento.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        } else {
            id_tratamiento = Integer.parseInt(tablaTratamiento.getValueAt(fila, 0).toString());         
            id_paciente = Integer.parseInt(tablaTratamiento.getValueAt(fila, 1).toString());
            String fecha = tablaTratamiento.getValueAt(fila, 2).toString();
            String tipo_diabetes = tablaTratamiento.getValueAt(fila, 3).toString();
            String condiciones_medicas = tablaTratamiento.getValueAt(fila, 4).toString();
            String medicamentos_dosis = tablaTratamiento.getValueAt(fila, 5).toString();
            String nivel_glucosa = tablaTratamiento.getValueAt(fila, 6).toString();
            Double peso = Double.parseDouble(tablaTratamiento.getValueAt(fila, 7).toString());
            String descripcion_dieta = tablaTratamiento.getValueAt(fila, 8).toString();
            
            //Configurar el id del paciente
            String nombrePaciente = daot.obtenerPacientePorId(id_paciente);
        if (nombrePaciente != null) {
            cmbPaciente.setSelectedItem(nombrePaciente);
        } else {
            // Manejar el caso en que no se encuentre el paciente
        }
            
            //Configurar la fecha de nacimiento
            txtFecha.setText(fecha);
            
            //Configurar el tipo de diabetes
            cmbTipo.setSelectedItem(tipo_diabetes);
            
            //Configurar las condiciones medicas
            txaCondiciones.setText(condiciones_medicas);
            
            //Configurar los medicamentos 
            txaMedicamentos.setText(medicamentos_dosis);
            
            //Configurar el nivel de glucosa
            txtGlucosa.setText(nivel_glucosa);
            
            //Configurar el peso
            txtPeso.setText(String.valueOf(peso));            

            //Configurar la descripcion de la dieta
            txaDieta.setText(descripcion_dieta);
        }
        
    }//GEN-LAST:event_tablaTratamientoMouseClicked

