void agregar() {
    //Instancia de la clase pacienteDAO
    tratamientoDAO daot = new tratamientoDAO(); 
        
    // Verificar si algÃºn campo obligatorio estÃ¡ vacÃ­o
    if (camposVacios()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // No se realiza la inserciÃ³n si hay campos vacÃ­os
    }

    //Tomar valor del nombre del paciente
    String nombrePaciente = (String) cmbPaciente.getSelectedItem();
    int id_paciente = daot.obtenerIdPacientePorNombre(nombrePaciente);
    
    //Tomar valor del campo de texto fecha
    String fecha = txtFecha.getText();
    
    //Tomar valor del combobox del tipo de diabetes
    String tipo_diabetes = (String) cmbTipo.getSelectedItem();
    
    //Tomar valor del campo condiciones medicas
    String condiciones_medicas = txaCondiciones.getText();
    
    //Tomar valor del campo medicamentos y dosis
    String medicamentos_dosis = txaMedicamentos.getText();
    
    //Tomar valor del campo nivel de glucosa
    String nivel_glucosa = txtGlucosa.getText();
    
    //Tomar valor del campo peso
    String pesoStr = txtPeso.getText();
    double peso = Double.parseDouble(pesoStr);

    //Tomar valor del campo dieta
    String descripcion_dieta = txaDieta.getText();

    // Resto del cÃ³digo para agregar el paciente...
    Object[] t = new Object[8];
    t[0] = id_paciente;
    t[1] = fecha;
    t[2] = tipo_diabetes;
    t[3] = condiciones_medicas;
    t[4] = medicamentos_dosis;
    t[5] = nivel_glucosa;
    t[6] = peso;
    t[7] = descripcion_dieta;

    tratamiento nuevoTratamiento = new tratamiento(0, id_paciente, fecha, tipo_diabetes, condiciones_medicas, medicamentos_dosis, nivel_glucosa, peso, descripcion_dieta);
    daot.add(nuevoTratamiento);
    JOptionPane.showMessageDialog(this, "Registro de tratamiento guardado correctamente");
    limpiar();
}

    // Verificar si algÃºn campo obligatorio estÃ¡ vacÃ­o
    boolean camposVacios() {
    return txtFecha.getText().isEmpty() || txtGlucosa.getText().isEmpty() || txtPeso.getText().isEmpty() || txaCondiciones.getText().isEmpty() || txaMedicamentos.getText().isEmpty() || txaDieta.getText().isEmpty();
}

    //Limpiar las cajas de texto
    void limpiar(){
        txtFecha.setText("");
        txtGlucosa.setText("");
        txtPeso.setText("");
        txaCondiciones.setText("");
        txaMedicamentos.setText("");
        txaDieta.setText("");
        // Para deseleccionar un JComboBox
        cmbPaciente.setSelectedIndex(-1);
        cmbTipo.setSelectedIndex(-1);

    }
    
